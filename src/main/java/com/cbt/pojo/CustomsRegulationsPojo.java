package com.cbt.pojo;

public class CustomsRegulationsPojo {
	private String country;
	private String regulation;
	private String clear_require;
	private String tax_code;
	private String re_provider;
	private String no_re_provider;
	private double amount;
	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getRegulation() {
		return regulation;
	}

	public void setRegulation(String regulation) {
		this.regulation = regulation;
	}

	public String getClear_require() {
		return clear_require;
	}

	public void setClear_require(String clear_require) {
		this.clear_require = clear_require;
	}

	public String getTax_code() {
		return tax_code;
	}

	public void setTax_code(String tax_code) {
		this.tax_code = tax_code;
	}

	public String getRe_provider() {
		return re_provider;
	}

	public void setRe_provider(String re_provider) {
		this.re_provider = re_provider;
	}

	public String getNo_re_provider() {
		return no_re_provider;
	}

	public void setNo_re_provider(String no_re_provider) {
		this.no_re_provider = no_re_provider;
	}

	public double getAmount() {
		return amount;
	}

	public void setAmount(double amount) {
		this.amount = amount;
	}

}
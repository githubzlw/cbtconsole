package com.cbt.forum.pojo;

public class ForumClassification {
	private Integer id;
	private String forumClassId;
	private String forumClassName;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getForumClassId() {
		return forumClassId;
	}

	public void setForumClassId(String forumClassId) {
		this.forumClassId = forumClassId;
	}

	public String getForumClassName() {
		return forumClassName;
	}

	public void setForumClassName(String forumClassName) {
		this.forumClassName = forumClassName;
	}
}
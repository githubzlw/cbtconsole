package com.cbt.website.bean;

public class GoodsSource {
	private int goodsId;
	private String goodsUrl;
	private String goodsPurl;
	private String goodsImg;
	private String goodsPrice;
	private String goodsName;
	private int moq;
	private String sourceType;
	private int orderDesc;
	public int getGoodsId() {
		return goodsId;
	}
	public void setGoodsId(int goodsId) {
		this.goodsId = goodsId;
	}
	public String getGoodsUrl() {
		return goodsUrl;
	}
	public void setGoodsUrl(String goodsUrl) {
		this.goodsUrl = goodsUrl;
	}
	public String getGoodsPurl() {
		return goodsPurl;
	}
	public void setGoodsPurl(String goodsPurl) {
		this.goodsPurl = goodsPurl;
	}
	public String getGoodsImg() {
		return goodsImg;
	}
	public void setGoodsImg(String goodsImg) {
		this.goodsImg = goodsImg;
	}
	public String getGoodsPrice() {
		return goodsPrice;
	}
	public void setGoodsPrice(String goodsPrice) {
		this.goodsPrice = goodsPrice;
	}
	public String getGoodsName() {
		return goodsName;
	}
	public void setGoodsName(String goodsName) {
		this.goodsName = goodsName;
	}
	public int getMoq() {
		return moq;
	}
	public void setMoq(int moq) {
		this.moq = moq;
	}
	public String getSourceType() {
		return sourceType;
	}
	public void setSourceType(String sourceType) {
		this.sourceType = sourceType;
	}
	public int getOrderDesc() {
		return orderDesc;
	}
	public void setOrderDesc(int orderDesc) {
		this.orderDesc = orderDesc;
	}
	
}
